require_relative 'hangman'

class AsciiHangman < Hangman

    def printable_state
      message = "You died!" if lost?
      message = "You survived." if solved?
      message ||= "Keep guessing"

      hangman_art + bad_guesses + <<-eos
: :
: : #{message.center(17)}
: : #{super.center(17)}
: :
      eos

    end

    private

    def hangman_art
      #probably should check for out-of-bounds
      @@art[wrong_guess_count]
    end

    def bad_guesses
      <<-eos
| |   #{wrong_guesses.ljust(7, '_').gsub(/./, '\0 ').rstrip}   | |
|_|___________________|_|
      eos
    end 

